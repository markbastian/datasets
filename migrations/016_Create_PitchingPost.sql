CREATE TABLE PitchingPost
(
    playerID VARCHAR(10) REFERENCES Master (playerID),
    yearID   INT REFERENCES Teams (yearID),
    round    VARCHAR(10),
    teamID   VARCHAR(3) REFERENCES Teams (teamID),
    lgID     VARCHAR(2) REFERENCES Leagues (lgID),
    W        INT,
    L        INT,
    G        INT,
    GS       INT,
    CG       INT,
    SHO      INT,
    SV       INT,
    IPouts   INT,
    H        INT,
    ER       INT,
    HR       INT,
    BB       INT,
    SO       INT,
    BAOpp    VARCHAR(10),
    ERA      VARCHAR(10),
    IBB      INT,
    WP       INT,
    HBP      INT,
    BK       INT,
    BFP      INT,
    GF       INT,
    R        INT,
    SH       INT,
    SF       INT,
    GIDP     INT,
    PRIMARY KEY (playerID, yearID, round)
);
